function buildColors(names: string[]): MaterialColor[] {
  return names.map((name, i) => ({
    id: `c-${i}-${name}`,
    name,
    hex: colorPalette[name] ?? "#CCCCCC",
  }));
}

function buildSizes(categoryId: string): MaterialSize[] {
  if (categoryId === "cat-3")
    return [
      { id: "s-1", label: "Формат", value: "60x60 см" },
      { id: "s-2", label: "Толщина", value: "9 мм" },
    ];
  if (categoryId === "cat-1")
    return [
      { id: "s-1", label: "Ширина рулона", value: "1.06 м" },
      { id: "s-2", label: "Длина рулона", value: "10 м" },
    ];
  if (categoryId === "cat-2")
    return [
      { id: "s-1", label: "Толщина", value: "8 мм" },
      { id: "s-2", label: "Класс износостойкости", value: "33" },
    ];
  return [{ id: "s-1", label: "Габариты", value: "Стандарт" }];
}

