function buildImages(seed: string): MaterialImage[] {
  return [1, 2, 3, 4].map((n) => ({
    id: `${seed}-img-${n}`,
    url: `https://picsum.photos/seed/${seed}-${n}/900/900`,
    alt: seed,
  }));
}

